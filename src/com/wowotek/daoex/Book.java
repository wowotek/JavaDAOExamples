package com.wowotek.daoex;

public class Book {
    public String Kode;
    public String Judul;
    public String ISBN;
    public String Pengarang;
    public String Penerbit;
    
    public Book(String Kode, String Judul, String ISBN, String Pengarang, String Penerbit){
	this.Kode = Kode;
	this.Judul = Judul;
	this.ISBN = ISBN;
	this.Pengarang = Pengarang;
	this.Penerbit = Penerbit;
    }
}
